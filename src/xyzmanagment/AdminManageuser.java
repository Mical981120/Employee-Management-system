    public ArrayList<User> userList (){
            ArrayList<User> userList = new ArrayList<>();
            try
        {
         DBConnection obj=new DBConnection();
         con=obj.GetConnection();
         PreparedStatement stmt=con.prepareStatement
         ("select * from UserInfo");
         
         ResultSet rs= stmt.executeQuery();
         User user;
         while(rs.next()){
             user=new User(rs.getString("UserID"),rs.getString("Password"),rs.getString("UserType"));
             userList.add(user);
         }
        }
        catch(Exception ex)
       {
            JOptionPane.showMessageDialog(null, "error connecting to database"+ ex,
                    "Register Form",JOptionPane.ERROR_MESSAGE);
       }
            return userList;
    }
    
    public void show_user (){
    ArrayList<User> list =  userList();
    DefaultTableModel model = (DefaultTableModel)jTableAdminUser.getModel();
    Object[] row= new Object[3];
    for(int i=0; i<list.size(); i++){
            row[0]=list.get(i).getUserID();
            row[1]=list.get(i).getPassword();
            row[2]=list.get(i).getUserType();
            model.addRow(row);
    }
        
    }

