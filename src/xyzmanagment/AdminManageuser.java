    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        try
        {
         if(txtUserID.getText().isEmpty())
            {
        JOptionPane.showMessageDialog(null,"Fill all the data ",
                "Login Form",JOptionPane.ERROR_MESSAGE);
        
            }
         PreparedStatement stat=con.prepareStatement
         ("delete from  UserInfo where UserID=?");
            stat.setString(1,txtUserID.getText());
         
                int numberRows=stat.executeUpdate();
                if(numberRows>0){
                    DefaultTableModel model = (DefaultTableModel)jTableAdminUser.getModel();
                    model.setRowCount(0);
                    show_user ();
                JOptionPane.showMessageDialog(null, "Deleted Succsessfully");
                }
         }
        
        catch(Exception ex)
       {
            JOptionPane.showMessageDialog(null, "error connecting while deleting data"+ ex,
                    "admin Form",JOptionPane.ERROR_MESSAGE);
       }
    }//GEN-LAST:event_btndeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
         try
        {
         boolean valid=true;
        if(txtUserID.getText().isEmpty()|| txtPassword.getText().toString().isEmpty())
            {
        JOptionPane.showMessageDialog(null,"Fill all the data ",
                "Login Form",JOptionPane.ERROR_MESSAGE);
        valid=false;
            }
         PreparedStatement stat=con.prepareStatement
         ("update UserInfo set Password=? , UserType=? where UserID=?");
            
            stat.setString(1,txtPassword.getText());
            stat.setString(2,cmbUserType.getSelectedItem().toString());
            stat.setString(3,txtUserID.getText());
         
                int numberRows=stat.executeUpdate();
                if(numberRows>0){
                    DefaultTableModel model = (DefaultTableModel)jTableAdminUser.getModel();
                    model.setRowCount(0);
                    show_user ();
                JOptionPane.showMessageDialog(null, "Updated Succsessfully");
                }
         }
        
        catch(Exception ex)
       {
            JOptionPane.showMessageDialog(null, "error connecting while Updating data"+ ex,
                    "admin Form",JOptionPane.ERROR_MESSAGE);
       }
    }//GEN-LAST:event_btnUpdateActionPerformed

